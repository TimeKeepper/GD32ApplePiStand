/*********************************************************************************************************
* 函数名称：ProcKeyDownKey1
* 函数功能：处理按键按下的事件，即按键按下的响应函数 
* 输入参数：void
* 输出参数：void
* 返 回 值：void
* 创建日期：2021年07月01日
* 注    意：
*********************************************************************************************************/
void  ProcKeyDownKey1(void)
{  
  OLEDShowString(0, 48, (const unsigned char *)"KEY1 PUSH DOWN");  //打印按键状态
}

